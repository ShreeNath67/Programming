//program to even or odd number using Ternary operator and syntax
//Use of Ternary Operator
#include<stdio.h>
int main()
{
    int n;
    printf("Enter any number :\n");
    scanf("%d",&n);
    n%2==0? printf("Even no. ") :printf("Odd no . ");           // Use of ternary operator                 
    return 0;
}